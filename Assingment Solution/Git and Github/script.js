function greet(name) {
    return `Hello, ${name}!`;
}

console.log(greet("World"));

function personalizedGreeting(name, age) {
    return `Hello, ${name}! You are ${age} years old.`;
}

console.log(personalizedGreeting("Alice", 25));


function farewell(name) {
    return `Goodbye, ${name}!`;
}

console.log(farewell("Alice"));

